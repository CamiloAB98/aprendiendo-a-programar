let a = 10; // Asignación simple (=): asigna el valor 10 a la variable a

a += 5; // Asignación de suma (+=): suma 5 a a (a = a + 5)

a -= 3; // Asignación de resta (-=): resta 3 a a (a = a - 3)

a *= 2; // Asignación de multiplicación (*=): multiplica a por 2 (a = a * 2)

a /= 4; // Asignación de división (/=): divide a entre 4 (a = a / 4)

a %= 3; // Asignación de residuo (%=): asigna el residuo de a dividido entre 3 (a = a % 3)

a **= 2; // Asignación de exponenciación (**=): eleva a a la potencia de 2 (a = a ** 2)

let b = 5;
b <<= 1; // Asignación de desplazamiento a la izquierda (<<=): desplaza los bits de b a la izquierda (b = b << 1)

b >>= 1; // Asignación de desplazamiento a la derecha (>>=): desplaza los bits de b a la derecha (b = b >> 1)

b >>>= 1; // Asignación de desplazamiento a la derecha sin signo (>>>=): desplaza los bits de b a la derecha sin signo (b = b >>> 1)

let c = 2;
c &= 1; // Asignación AND bit a bit (&=): realiza una operación AND bit a bit entre c y 1 (c = c & 1)

c |= 2; // Asignación OR bit a bit (|=): realiza una operación OR bit a bit entre c y 2 (c = c | 2)

c ^= 3; // Asignación XOR bit a bit (^=): realiza una operación XOR bit a bit entre c y 3 (c = c ^ 3)